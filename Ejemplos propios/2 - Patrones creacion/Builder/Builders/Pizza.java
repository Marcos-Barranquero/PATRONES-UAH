package Builders;

public class Pizza
{
    private String ingredientres;
    private String masa;
    private String nombre;

    public Pizza()
    {
    }

    public String getIngredientres()
    {
        return ingredientres;
    }

    public String getMasa()
    {
        return masa;
    }

    public String getNombre()
    {
        return nombre;
    }


    public void setIngredientres(String ingredientres)
    {
        this.ingredientres = ingredientres;
    }

    public void setMasa(String masa)
    {
        this.masa = masa;
    }

    public void setNombre(String nombre)
    {
        this.nombre = nombre;
    }

}
